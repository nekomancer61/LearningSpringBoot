public class Road {
    private int length;
    private int type;

    

    public Road(int length, int type) {
        this.length = length;
        this.type = type;
    }
    public int getLength() {
        return length;
    }
    public void setLength(int length) {
        this.length = length;
    }
    public int getType() {
        return type;
    }
    public void setType(int type) {
        this.type = type;
    }
    
}
